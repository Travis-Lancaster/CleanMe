import type { AppRouteRecordRaw } from "#src/app/router/types";

import ContainerLayout from "#src/app/layout/container-layout";
import { $t } from "#src/app/locales";
import { exception, exception403Path, exception404Path, exception500Path, exceptionPath, exceptionUnknownComponentPath } from "#src/app/router/extra-info";
import { lazy } from "react";

const Exception403 = lazy(() => import("#src/app/pages/exception/403"));
